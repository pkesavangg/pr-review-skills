import SwiftUI

// MARK: - IconSize
/// Represents the size of an icon.
struct IconSize {
    let width: CGFloat
    let height: CGFloat
    
    init(width: CGFloat = 24, height: CGFloat = 24) {
        self.width = width
        self.height = height
    }
}
