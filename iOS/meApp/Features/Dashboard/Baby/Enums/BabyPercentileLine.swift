import Foundation

enum BabyPercentileLine: String, CaseIterable {
    case fifth = "5th"
    case tenth = "10th"
    case twentyFifth = "25th"
    case fiftieth = "50th"
    case seventyFifth = "75th"
    case ninetieth = "90th"
    case ninetyFifth = "95th"
}
